<template>
  <div>
      <div class="header">
          <p>最近播放</p>
      </div>
      <router-link to="/">
      <img src="../public/icons/left.png" alt="" class="back">
      </router-link>
      <ul>
          <li v-for="data in musiclist" @click="play(data.id,data.name,data.arName,data.picUrl)">
            <p class="name">{{data.name}}</p>  
            <p class="arName">{{data.arName}} · {{data.alName}}</p>  
          </li>
      </ul>
  </div>
</template>
<script>
export default {
    computed:{
        musiclist(){
            return this.$store.state.musiclist
        }
    },
    methods:{
        play(id,name,arName,picUrl,alName){
            this.$store.state.name=name
            this.$store.state.arName=arName
            this.$store.state.picUrl=picUrl
            // this.$store.state.alName=alName
            this.$store.state.isPlay = true
           this.$store.state.avatarShow = false 
           this.$store.state.avatar1Show = true
           this.$store.state.rotImg1Show = true
           this.$store.state.rotImgShow = false
           this.axios.post("https://api.imjad.cn/cloudmusic/?type=song&id=" + id +"&br=128000")
           .then(res=>{
               if(200===res.data.code){
                   this.$store.state.src=res.data.data[0].url
               }
           })
           

        }
    }
}
</script>
